function applyTranslation(langCode) {
    const t = translations[langCode];
    if (!t) return;

    // Apply translations for data-translate-key attributes
    document.querySelectorAll('[data-translate-key]').forEach(element => {
        const key = element.getAttribute('data-translate-key');
        if (t[key]) {
            element.textContent = t[key];
        }
    });

    // Handle attribute translations (e.g., placeholder, title if needed)
    // For now, only textContent is handled as per the provided structure.

    // Set active language button style
    document.querySelectorAll('.language-selector button').forEach(button => {
        const buttonLangCode = button.getAttribute('onclick').match(/'(\w+)'/)[1];
        if (buttonLangCode === langCode) {
            button.classList.add('active');
        } else {
            button.classList.remove('active');
        }
    });
}


function setLanguage(langCode) {
    localStorage.setItem('selectedLanguage', langCode);
    applyTranslation(langCode);
}


